// 02. Numbers N-1.cpp : This file contains the 'main' function. Program execution begins and ends there.
//

#include <iostream>
using namespace std;

int main()
{
	int n;
	cin >> n;

	for (int i = n; i > 0; i--) {
		cout << i << endl;
	}

	return 0;
}

