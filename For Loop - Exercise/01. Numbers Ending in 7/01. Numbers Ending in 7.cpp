// 01. Numbers Ending in 7.cpp : This file contains the 'main' function. Program execution begins and ends there.
//

#include <iostream>
using namespace std;

int main()
{
	for (int i = 7; i < 1000; i += 10) {
		cout << i << endl;
	}

	return 0;
}

